    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        if (this.jTable1.getSelectedRowCount() == 1) {
            Descargas desc = descargasList.get(jTable1.getSelectedRow());
            if (desc.getTamborCollection().isEmpty()) {
                if (JOptionPane.showConfirmDialog(null, "Desea borrar la descarga?", "Atención", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                    try {
                        controladorD.destroy(desc.getIdDescargas());
                        JOptionPane.showMessageDialog(null, "Descarga borrada");
                        actualizarTabla();
                    } catch (Exception ex) {
                        JOptionPane.showMessageDialog(null, "Error al borrar la descarga", "Información", JOptionPane.ERROR_MESSAGE);
                        Logger.getLogger(jDialogDescargas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

            } else {
                JOptionPane.showMessageDialog(null, "No se puede borrar la descarga, tiene tambores asociados", "Validación", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una descarga", "Validación", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jBBorrarActionPerformed

