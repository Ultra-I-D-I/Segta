    private void jBBuscarDescargaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBuscarDescargaActionPerformed

        String fechaDesde = dCCfechaDesde.getText();
        String fechaHasta = dCCfechaHasta.getText();
        String paramCamion = '%' + this.jTFBuscarCamion.getText() + '%';
        Date fechaD = null;
        Date fechaH = null;
        try {
            fechaD = (Date) new SimpleDateFormat("dd/MM/yy").parse(fechaDesde);
        } catch (ParseException ex) {
            Logger.getLogger(jDialogDescargas.class.getName()).log(Level.SEVERE, null, ex);
        }

        try {
            fechaH = (Date) new SimpleDateFormat("dd/MM/yy").parse(fechaHasta);
        } catch (ParseException ex) {
            Logger.getLogger(jDialogDescargas.class.getName()).log(Level.SEVERE, null, ex);
        }

        Query queryDesc = SegTAPUEntityManager.createQuery("SELECT d FROM Descargas d WHERE d.camion like :camion AND  d.fecha BETWEEN :fechaDesde AND :fechaHasta ORDER BY d.fecha DESC");
        queryDesc.setParameter("fechaDesde", fechaD);
        queryDesc.setParameter("fechaHasta", fechaH);
        queryDesc.setParameter("camion", paramCamion);

        descargasQuery = java.beans.Beans.isDesignTime() ? null : queryDesc;
        descargasList.clear();
        descargasList.addAll(descargasQuery.getResultList());
    }//GEN-LAST:event_jBBuscarDescargaActionPerformed

