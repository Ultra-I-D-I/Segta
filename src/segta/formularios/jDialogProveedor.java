    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        
        int flagValidacion = 0;
        String razSoc = this.jTextRazonSocial.getText();
        BigInteger cuit = null;
        BigInteger tel = null;

        //Razon social no null
        if (razSoc.isEmpty()) {
            flagValidacion = 1;
            JOptionPane.showMessageDialog(null, "Ingrese una RazÃ³n Social", "ValidaciÃ³n", JOptionPane.WARNING_MESSAGE);
            this.jTextRazonSocial.requestFocus();
        }
        //cuit numerico
        if (!this.jTextCUIT.getText().isEmpty()) {

            try {
                cuit = new BigInteger(this.jTextCUIT.getText());

            } catch (Exception ex) {
                flagValidacion = 1;
                JOptionPane.showMessageDialog(null, "Ingrese sÃ³lo nÃºmeros", "InformaciÃ³n", JOptionPane.WARNING_MESSAGE);
                this.jTextCUIT.requestFocus();

            }
        }

        //telefono numerico
        if (!this.jTextTelefono.getText().isEmpty()) {

            try {
                tel = new BigInteger(this.jTextTelefono.getText());

            } catch (Exception ex) {
                flagValidacion = 1;
                JOptionPane.showMessageDialog(null, "Ingrese sÃ³lo nÃºmeros", "InformaciÃ³n", JOptionPane.WARNING_MESSAGE);
                this.jTextTelefono.requestFocus();

            }
        }
        // Localliad seleccionada
        loc = localidadList.get(this.jComboLocalidad.getSelectedIndex());
        if (loc.getIdLocalidad() == null) {
            flagValidacion = 1;
            JOptionPane.showMessageDialog(null, "Seleccione Localidad", "InformaciÃ³n", JOptionPane.WARNING_MESSAGE);
            jComboLocalidad.requestFocus();
        }

        if (flagValidacion == 0) {

            if (tipo.equals("nuevo")) {
                Proveedor nuevoProv = new Proveedor();
                nuevoProv.setRazonSocial(razSoc);
                nuevoProv.setCuit(cuit);
                nuevoProv.setDireccion(this.jTextDireccion.getText());
                nuevoProv.setTelefono(tel);
                nuevoProv.setMail(this.jTextMail.getText());
                nuevoProv.setIdLocalidad(loc);
                nuevoProv.setRenapa(this.jTextRenapa.getText());
                nuevoProv.setBaja(0);
                if (jCBAcopiador.isSelected()) {
                    nuevoProv.setAcopiador(true);
                }

                try {

                    controlador.create(nuevoProv);
                    JOptionPane.showMessageDialog(null, "Apicultor creado con Ã©xito", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                    limpiaCampos();

                } catch (Exception ex) {
                    Logger.getLogger(jDialogProveedor.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error al crear el apicultor", "InformaciÃ³n", JOptionPane.ERROR_MESSAGE);
                }
            }

            if (tipo.equals("edit")) {
                prov.setRazonSocial(razSoc);
                prov.setCuit(cuit);
                prov.setDireccion(this.jTextDireccion.getText());
                prov.setTelefono(tel);
                prov.setMail(this.jTextMail.getText());
                prov.setIdLocalidad(loc);
                prov.setRenapa(this.jTextRenapa.getText());
                if (jCBAcopiador.isSelected()) {
                    prov.setAcopiador(true);
                } else {
                    prov.setAcopiador(false);
                }
                try {

                    controlador.edit(prov);
                    JOptionPane.showMessageDialog(null, "Apicultor editado con Ã©xito", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);
                    limpiaCampos();

                } catch (Exception ex) {
                    Logger.getLogger(jDialogProveedor.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(null, "Error al editar el apicultor", "InformaciÃ³n", JOptionPane.ERROR_MESSAGE);
                }

            }
            limpiaCampos();
            desactivaCampos();
            activaTabla();
            actualizaTabla();

        }

    }//GEN-LAST:event_jBAceptarActionPerformed
