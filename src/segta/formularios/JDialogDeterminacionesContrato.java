public void actualizaTablaDeterminacion() {
        contratodeterminacionList.clear();
        contratodeterminacionList.addAll(contratodeterminacionQuery.getResultList());
    }
    private void jBAgregar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAgregar1ActionPerformed
        code = new Contratodeterminacion();
        Determinacion de = (Determinacion) this.jCBDeterminacion.getSelectedItem();
        try {
            code.setIdDeterminacion(de);
            code.setIdContrato(contrato);
            controladorCD.create(code);
            actualizaTablaDeterminacion();
        } catch (Exception e) {

        }

    }//GEN-LAST:event_jBAgregar1ActionPerformed

    private void jBBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBBorrarActionPerformed
        if (this.jTableDeterminacion.getSelectedRowCount() !=1) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar una Determinaci√≥");
            jTableDeterminacion.requestFocus();

        } else {
         
            try {
                code = contratodeterminacionList.get(jTableDeterminacion.getSelectedRow());
                controladorCD.destroy(code.getIdContratoDeterminacion());
            } catch (NonexistentEntityException ex) {
                Logger.getLogger(JDialogDeterminacionesContrato.class.getName()).log(Level.SEVERE, null, ex);
            }
            actualizaTablaDeterminacion();
        }
    }//GEN-LAST:event_jBBorrarActionPerformed

