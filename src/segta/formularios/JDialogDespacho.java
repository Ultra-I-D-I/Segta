    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        
        if (jTextRemito.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe ingresar el remito!", "Validaci√≥n", JOptionPane.WARNING_MESSAGE);
        }else{
        
        String fechaS = this.dateChooserCombo.getText();
        try {
            Date fechaD = (Date) new SimpleDateFormat("dd/MM/yy").parse(fechaS);
            //Actualizo el estado de los tambores del lote
            for (Tambor t : lote.getTamborCollection()) {
                t.setEstado("despachado");
                controladorT.edit(t);
            }

            lote.setDespacho(fechaD);
            lote.setRemito(jTextRemito.getText());
            lote.setCamion(jTextCamion.getText());
            controladorL.edit(lote);
            JOptionPane.showMessageDialog(null, "Lote Despachado!");
            this.processWindowEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error al despachar el lote!", "Validaci√≥n", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(jDialogTamboresGeneral.class.getName()).log(Level.SEVERE, null, ex);
        }   
        
        }

    }//GEN-LAST:event_jBAceptarActionPerformed

