            try {
                Calendar cal = Calendar.getInstance();
                cal.setTime(anali.getFecha());
                this.dCFecha.setSelectedDate(cal);
            } catch (Exception ex) {
                Logger.getLogger(jDialogAnalisis.class.getName()).log(Level.SEVERE, null, ex);
            }

