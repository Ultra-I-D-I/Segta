    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed
        
        boolean valido=true;
        int cLotes = 0, mTambores=0,cItem=0;
        try{
            cItem=Integer.parseInt(this.jTextFieldItem.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un número", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldItem.requestFocus();
            valido=false;
            Logger.getLogger(jDialogTamboresGeneral.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        try{
            cLotes=Integer.parseInt(this.jTextFieldLotes.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un número", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldLotes.requestFocus();
            valido=false;
            Logger.getLogger(jDialogTamboresGeneral.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        try{
            mTambores=Integer.parseInt(this.jTextFieldTambores.getText());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Debe ingresar un número", "Validación", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldTambores.requestFocus();
            valido=false;
            Logger.getLogger(jDialogTamboresGeneral.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if (valido){
            try {
                String fechaS = this.dateChooserCombo.getText();
                Date fechaD = null;
                try {
                    fechaD = (Date) new SimpleDateFormat("dd/MM/yyyy").parse(fechaS);
                    lotecont.setFechaEntrega(fechaD);
                } catch (ParseException ex) {
                    Logger.getLogger(jDialogAnalisis.class.getName()).log(Level.SEVERE, null, ex);
                }
                lotecont.setColor(this.jComboBoxColor.getSelectedItem().toString());
                lotecont.setCantLotes(cLotes);
                lotecont.setMaxTambores(mTambores);
                lotecont.setItem(cItem);
                if (accion.equals("nuevo")){
                    controladorT.create(lotecont);
                }else{
                    controladorT.edit(lotecont);
                }
                this.processWindowEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error al actualizar los datos", "Validación", JOptionPane.WARNING_MESSAGE);
                Logger.getLogger(jDialogTamboresGeneral.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jBAceptarActionPerformed

