    private void setearLoteo() {
        
        if (accion.equals("nuevo")){
            lotecont.setIdContrato(pContrato);
            lotecont.setEstado("abierto");
        }else{
            lotecont=pLoteo;
            this.jComboBoxColor.setSelectedItem(lotecont.getColor());
            try{
                Calendar cal = Calendar.getInstance();
                cal.setTime(lotecont.getFechaEntrega());
                this.dateChooserCombo.setSelectedDate(cal);
            } catch (Exception ex) {
                Logger.getLogger(jDialogAnalisis.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.jTextFieldLotes.setText(lotecont.getCantLotes().toString());
            this.jTextFieldTambores.setText(lotecont.getMaxTambores().toString());
            this.jTextFieldItem.setText(lotecont.getItem().toString());
        }
    }
